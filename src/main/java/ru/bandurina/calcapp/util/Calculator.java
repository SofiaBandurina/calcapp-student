package ru.bandurina.calcapp.util;

public class Calculator {

    public static int summ(int num1, int num2) {//сумма
        return num1 + num2;
    }

    public static int diff(int num1, int num2) {//разность
        return num1 - num2;
    }

    public static int comp(int num1, int num2) {//произведение
        return num1 * num2;
    }
}
